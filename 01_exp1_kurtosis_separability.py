def parse_train_protocol(txt_path):
    """Returns {utt_id: label} where 0=bonafide, 1=spoof."""
    labels = {}
    with open(txt_path) as f:
        for line in f:
            parts = line.strip().split()
            if len(parts) < 5:
                continue
            labels[parts[1]] = 0 if parts[4] == "bonafide" else 1
    bon = sum(1 for v in labels.values() if v == 0)
    spo = sum(1 for v in labels.values() if v == 1)
    print(f"Protocol: {len(labels)} utterances  "
          f"(bonafide={bon}, spoof={spo})")
    return labels

