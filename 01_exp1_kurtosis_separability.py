def load_kurtosis_from_h5(h5_path, labels_dict):
    """
    Loads per-channel kurtosis (mean pooling stat) from HDF5.

    Returns:
      kurt_fine  : (N, 24) float32 — fine-structure kurtosis mean per channel
      kurt_env   : (N, 24) float32 — envelope kurtosis mean per channel
      y          : (N,) int32      — 0=bonafide, 1=spoof
      utt_ids    : list of str
    """
    kurt_fine_list, kurt_env_list, y_list, ids_list = [], [], [], []

    with h5py.File(h5_path, 'r') as hf:
        utt_ids = list(hf.keys())
        for utt_id in tqdm(utt_ids, desc="Loading HDF5", ncols=80):
            label = labels_dict.get(utt_id, -1)
            if label == -1:
                continue
            feat = hf[utt_id]['feat'][:]          # (2496,)

            # Extract kurtosis mean per channel using layout map
            fine_means = np.array([
                feat[ch * DIMS_PER_CHAN + KURT_FINE_OFFSET + POOL_MEAN_IDX]
                for ch in range(N_CHANNELS)
            ], dtype=np.float32)

            env_means = np.array([
                feat[ch * DIMS_PER_CHAN + KURT_ENV_OFFSET + POOL_MEAN_IDX]
                for ch in range(N_CHANNELS)
            ], dtype=np.float32)

            kurt_fine_list.append(fine_means)
            kurt_env_list.append(env_means)
            y_list.append(label)
            ids_list.append(utt_id)

    kurt_fine = np.array(kurt_fine_list, dtype=np.float32)
    kurt_env  = np.array(kurt_env_list,  dtype=np.float32)
    y         = np.array(y_list,         dtype=np.int32)

    bon = (y == 0).sum()
    spo = (y == 1).sum()
    print(f"Loaded: {len(y)} utterances  "
          f"(bonafide={bon}, spoof={spo})  "
          f"Shape: {kurt_fine.shape}")
    return kurt_fine, kurt_env, y, ids_list

