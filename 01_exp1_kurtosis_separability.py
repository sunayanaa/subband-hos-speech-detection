def cohens_d(x1, x2):
    """
    Cohen's d: standardised mean difference.
    Uses pooled standard deviation (Hedges' formulation for unequal N).
    Positive values mean x1 > x2.
    """
    n1, n2   = len(x1), len(x2)
    mean_diff = np.mean(x1) - np.mean(x2)
    pooled_std = np.sqrt(
        ((n1 - 1) * np.var(x1, ddof=1) + (n2 - 1) * np.var(x2, ddof=1))
        / (n1 + n2 - 2)
    )
    if pooled_std < 1e-12:
        return 0.0
    return float(mean_diff / pooled_std)

def separability_analysis(kurt_matrix, y, component_name):
    """
    Per-channel separability: Cohen's d + Mann-Whitney U + Bonferroni.

    Args:
      kurt_matrix : (N, 24) kurtosis values
      y           : (N,) labels (0=bonafide, 1=spoof)
      component_name: 'fine_structure' or 'envelope'

    Returns dict with per-channel stats.
    """
    bon_idx = np.where(y == 0)[0]
    spo_idx = np.where(y == 1)[0]
    n_tests  = N_CHANNELS
    alpha    = 0.05
    alpha_bf = alpha / n_tests   # Bonferroni threshold

    results = {
        'component'    : component_name,
        'n_bonafide'   : int(len(bon_idx)),
        'n_spoof'      : int(len(spo_idx)),
        'n_tests'      : n_tests,
        'alpha_bonferroni': float(alpha_bf),
        'channels'     : []
    }

    print(f"\n  {component_name}:")
    print(f"  {'Ch':>3}  {'Freq(Hz)':>9}  {'d':>7}  {'p-raw':>10}  "
          f"{'p-BF':>10}  {'sig':>4}  "
          f"{'mean_bon':>9}  {'mean_spo':>9}")
    print("  " + "-" * 75)

    for ch in range(N_CHANNELS):
        bon_vals = kurt_matrix[bon_idx, ch]
        spo_vals = kurt_matrix[spo_idx, ch]

        d    = cohens_d(bon_vals, spo_vals)
        stat, p_raw = st.mannwhitneyu(bon_vals, spo_vals,
                                       alternative='two-sided')
        p_bf  = min(p_raw * n_tests, 1.0)   # Bonferroni-corrected p
        sig   = p_bf < alpha

        ch_result = {
            'channel_idx'       : ch,
            'centre_freq_hz'    : float(CHANNEL_CENTRES_HZ[ch]),
            'cohens_d'          : round(d, 4),
            'mannwhitney_u'     : float(stat),
            'p_raw'             : float(p_raw),
            'p_bonferroni'      : float(p_bf),
            'significant'       : bool(sig),
            'mean_bonafide'     : float(np.mean(bon_vals)),
            'mean_spoof'        : float(np.mean(spo_vals)),
            'std_bonafide'      : float(np.std(bon_vals)),
            'std_spoof'         : float(np.std(spo_vals)),
            'median_bonafide'   : float(np.median(bon_vals)),
            'median_spoof'      : float(np.median(spo_vals)),
        }
        results['channels'].append(ch_result)

        sig_str = "***" if sig else "   "
        print(f"  {ch:>3}  {CHANNEL_CENTRES_HZ[ch]:>9.0f}  "
              f"{d:>7.3f}  {p_raw:>10.2e}  {p_bf:>10.2e}  "
              f"{sig_str:>4}  "
              f"{np.mean(bon_vals):>9.3f}  {np.mean(spo_vals):>9.3f}")

    n_sig = sum(1 for c in results['channels'] if c['significant'])
    print(f"\n  Significant channels (Bonferroni): {n_sig}/{N_CHANNELS}")

    return results

