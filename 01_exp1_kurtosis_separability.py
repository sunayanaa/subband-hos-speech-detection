def fig_cohens_d_heatmap(fine_results, env_results, out_path):
    """
    Figure 1: 2×24 heatmap of Cohen's d values.
    Rows: fine-structure, envelope. Columns: channels (labelled in Hz).
    """
    d_fine = np.array([c['cohens_d'] for c in fine_results['channels']])
    d_env  = np.array([c['cohens_d'] for c in env_results['channels']])
    matrix = np.vstack([d_fine, d_env])   # (2, 24)

    # Diverging colormap centred at 0
    vmax = max(abs(matrix).max(), 0.5)
    norm = TwoSlopeNorm(vmin=-vmax, vcenter=0, vmax=vmax)

    fig, ax = plt.subplots(figsize=(13, 2.8))
    im = ax.imshow(matrix, aspect='auto', cmap='RdBu_r', norm=norm)

    # x-axis: channel frequencies (rounded to nearest 10 Hz)
    freq_labels = [f"{int(round(f/10)*10)}" for f in CHANNEL_CENTRES_HZ]
    ax.set_xticks(range(N_CHANNELS))
    ax.set_xticklabels(freq_labels, rotation=45, ha='right', fontsize=7)
    ax.set_yticks([0, 1])
    ax.set_yticklabels(['Fine-structure', 'Envelope'], fontsize=10)
    ax.set_xlabel("Gammatone Channel Centre Frequency (Hz)")
    ax.set_title("Cohen's $d$ — Bonafide vs. Spoof Kurtosis per Subband",
                 pad=8)

    # Annotate each cell with d value
    for i in range(2):
        for j in range(N_CHANNELS):
            val = matrix[i, j]
            color = 'white' if abs(val) > 0.6 * vmax else 'black'
            ax.text(j, i, f'{val:.2f}', ha='center', va='center',
                    fontsize=6, color=color)

    # Mark significance with asterisk below heatmap
    for j, ch in enumerate(fine_results['channels']):
        if ch['significant']:
            ax.text(j, 1.52, '*', ha='center', va='bottom',
                    fontsize=9, color='black', fontweight='bold')

    cbar = plt.colorbar(im, ax=ax, fraction=0.02, pad=0.02)
    cbar.set_label("Cohen's $d$", fontsize=9)
    fig.tight_layout()
    fig.savefig(out_path, dpi=300, bbox_inches='tight')
    plt.close(fig)
    print(f"Saved: {os.path.basename(out_path)}")


def fig_kurtosis_violin(kurt_fine, y, fine_results, out_path):
    """
    Figure 2: Violin plots for the 8 most discriminative channels
    (by |Cohen's d| on fine-structure kurtosis).
    """
    # Rank channels by |d|
    d_vals   = np.array([abs(c['cohens_d']) for c in fine_results['channels']])
    top8_idx = np.argsort(d_vals)[::-1][:8]

    bon_idx = np.where(y == 0)[0]
    spo_idx = np.where(y == 1)[0]

    fig, axes = plt.subplots(2, 4, figsize=(13, 6), sharey=False)
    axes = axes.flatten()

    for rank, ch in enumerate(top8_idx):
        ax    = axes[rank]
        bon_v = kurt_fine[bon_idx, ch]
        spo_v = kurt_fine[spo_idx, ch]

        # Clip extreme outliers for display only
        p1, p99 = np.percentile(np.concatenate([bon_v, spo_v]), [1, 99])
        bon_v_clip = np.clip(bon_v, p1, p99)
        spo_v_clip = np.clip(spo_v, p1, p99)

        parts = ax.violinplot([bon_v_clip, spo_v_clip],
                               positions=[0, 1],
                               showmedians=True,
                               showextrema=False)

        parts['bodies'][0].set_facecolor(C_BON)
        parts['bodies'][0].set_alpha(0.7)
        parts['bodies'][1].set_facecolor(C_SPO)
        parts['bodies'][1].set_alpha(0.7)
        parts['cmedians'].set_color('black')
        parts['cmedians'].set_linewidth(1.5)

        d_ch  = fine_results['channels'][ch]['cohens_d']
        sig   = fine_results['channels'][ch]['significant']
        freq  = CHANNEL_CENTRES_HZ[ch]
        title = (f"Ch {ch}  ({freq:.0f} Hz)\n"
                 f"$d$={d_ch:.2f}{'*' if sig else ''}")
        ax.set_title(title, fontsize=9)
        ax.set_xticks([0, 1])
        ax.set_xticklabels(['Bonafide', 'Spoof'], fontsize=8)
        ax.set_ylabel("Kurtosis (fine-str.)", fontsize=7)
        ax.grid(True, axis='y', linestyle='--', alpha=0.4)

    fig.suptitle("Kurtosis Fine-Structure Distribution — Top 8 Discriminative Channels",
                 fontsize=11, fontweight='bold', y=1.01)
    bon_patch = mpatches.Patch(color=C_BON, alpha=0.7, label='Bonafide')
    spo_patch = mpatches.Patch(color=C_SPO, alpha=0.7, label='Spoof')
    fig.legend(handles=[bon_patch, spo_patch], loc='upper right',
               fontsize=9, bbox_to_anchor=(1.0, 1.0))
    fig.tight_layout()
    fig.savefig(out_path, dpi=300, bbox_inches='tight')
    plt.close(fig)
    print(f"Saved: {os.path.basename(out_path)}")


def fig_mean_profile(kurt_fine, kurt_env, y, out_path):
    """
    Figure 3: Mean kurtosis profile vs channel frequency for bonafide and spoof.
    Two panels: fine-structure (top), envelope (bottom).
    """
    bon_idx = np.where(y == 0)[0]
    spo_idx = np.where(y == 1)[0]
    freqs   = CHANNEL_CENTRES_HZ

    fig, axes = plt.subplots(2, 1, figsize=(10, 6), sharex=True)

    for ax, kurt_mat, comp_label in zip(
            axes,
            [kurt_fine, kurt_env],
            ['Fine-structure kurtosis', 'Envelope kurtosis']):

        bon_mean = kurt_mat[bon_idx].mean(axis=0)
        bon_std  = kurt_mat[bon_idx].std(axis=0)
        spo_mean = kurt_mat[spo_idx].mean(axis=0)
        spo_std  = kurt_mat[spo_idx].std(axis=0)

        ax.fill_between(freqs, bon_mean - bon_std, bon_mean + bon_std,
                        color=C_BON, alpha=0.15)
        ax.fill_between(freqs, spo_mean - spo_std, spo_mean + spo_std,
                        color=C_SPO, alpha=0.15)
        ax.plot(freqs, bon_mean, 'o-', color=C_BON, lw=1.8,
                ms=4, label='Bonafide')
        ax.plot(freqs, spo_mean, 's--', color=C_SPO, lw=1.8,
                ms=4, label='Spoof')
        ax.axhline(0, color='black', lw=0.7, ls=':', alpha=0.5)
        ax.set_ylabel(f"Mean excess {comp_label.lower()}")
        ax.set_title(comp_label)
        ax.legend(fontsize=9)
        ax.grid(True, linestyle='--', alpha=0.35)
        ax.set_xscale('log')

    axes[-1].set_xlabel("Channel Centre Frequency (Hz)")
    fig.suptitle("Mean Subband Kurtosis Profile: Bonafide vs Spoof",
                 fontsize=12, fontweight='bold')
    fig.tight_layout()
    fig.savefig(out_path, dpi=300, bbox_inches='tight')
    plt.close(fig)
    print(f"Saved: {os.path.basename(out_path)}")


def fig_cohens_d_barplot(fine_results, env_results, out_path):
    """
    Figure 4: Cohen's d bar chart, channels sorted by |d| (fine-structure).
    Colour indicates significance after Bonferroni correction.
    """
    channels   = fine_results['channels']
    order      = sorted(range(N_CHANNELS),
                        key=lambda i: abs(channels[i]['cohens_d']),
                        reverse=True)

    d_fine_ord = [channels[i]['cohens_d']     for i in order]
    d_env_ord  = [env_results['channels'][i]['cohens_d'] for i in order]
    sig_ord    = [channels[i]['significant']   for i in order]
    freq_ord   = [f"{CHANNEL_CENTRES_HZ[i]:.0f} Hz" for i in order]

    colors_fine = [C_BON if s else '#90CAF9' for s in sig_ord]
    colors_env  = [C_SPO if s else '#EF9A9A' for s in sig_ord]

    x = np.arange(N_CHANNELS)
    w = 0.4

    fig, ax = plt.subplots(figsize=(14, 4.5))
    ax.bar(x - w/2, d_fine_ord, w, color=colors_fine,
           label='Fine-structure', edgecolor='white', linewidth=0.3)
    ax.bar(x + w/2, d_env_ord,  w, color=colors_env,
           label='Envelope', edgecolor='white', linewidth=0.3,
           hatch='//')
    ax.axhline(0, color='black', lw=0.7)

    ax.set_xticks(x)
    ax.set_xticklabels(freq_ord, rotation=55, ha='right', fontsize=7)
    ax.set_ylabel("Cohen's $d$  (bonafide − spoof)")
    ax.set_title("Cohen's $d$ per Subband — Ranked by Discriminability\n"
                 "(Dark = significant after Bonferroni; "
                 "Light = not significant)",
                 fontsize=10)
    ax.legend(fontsize=9)
    ax.grid(True, axis='y', linestyle='--', alpha=0.4)
    fig.tight_layout()
    fig.savefig(out_path, dpi=300, bbox_inches='tight')
    plt.close(fig)
    print(f"Saved: {os.path.basename(out_path)}")

